drop function if exists public.increment_idea_post_view(bigint);

create or replace function public.increment_idea_post_view(target_post_id bigint)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  inserted boolean := false;
begin
  insert into public.idea_post_views (post_id, user_id)
  values (target_post_id, auth.uid())
  on conflict (post_id, user_id) do nothing;

  inserted := found;

  if inserted then
    update public.idea_posts
    set view_count = view_count + 1
    where id = target_post_id;
  end if;

  return inserted;
end;
$$;

create or replace function public.backfill_idea_post_view_counts()
returns void
language sql
security definer
set search_path = public
as $$
  update public.idea_posts
  set view_count = coalesce(view_counts.count_value, 0)::integer
  from (
    select post.id, count(view_record.id) as count_value
    from public.idea_posts post
    left join public.idea_post_views view_record on view_record.post_id = post.id
    group by post.id
  ) view_counts
  where public.idea_posts.id = view_counts.id
    and exists (
      select 1
      from public.profiles p
      where p.id = auth.uid()
        and p.role = 'admin'
    );
$$;

grant execute on function public.increment_idea_post_view(bigint) to authenticated;
grant execute on function public.backfill_idea_post_view_counts() to authenticated;

