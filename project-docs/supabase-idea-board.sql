create or replace function public.get_idea_attachment_admin_summary()
returns table (
  attachment_count bigint,
  total_bytes bigint
)
language sql
security definer
set search_path = public
as $$
  select
    count(*)::bigint as attachment_count,
    coalesce(sum(size_bytes), 0)::bigint as total_bytes
  from public.idea_attachments
  where exists (
    select 1
    from public.profiles p
    where p.id = auth.uid()
      and p.role = 'admin'
  );
$$;

grant execute on function public.get_idea_attachment_admin_summary() to authenticated;

create or replace function public.get_idea_board_admin_summary()
returns table (
  post_count bigint,
  comment_count bigint,
  reaction_count bigint,
  recent_post_count bigint
)
language sql
security definer
set search_path = public
as $$
  select
    (select count(*) from public.idea_posts)::bigint as post_count,
    (select count(*) from public.idea_comments)::bigint as comment_count,
    (
      (select count(*) from public.idea_reactions)
      + (select count(*) from public.idea_comment_reactions)
    )::bigint as reaction_count,
    (select count(*) from public.idea_posts where created_at >= now() - interval '30 days')::bigint as recent_post_count
  where exists (
    select 1
    from public.profiles p
    where p.id = auth.uid()
      and p.role = 'admin'
  );
$$;

grant execute on function public.get_idea_board_admin_summary() to authenticated;

